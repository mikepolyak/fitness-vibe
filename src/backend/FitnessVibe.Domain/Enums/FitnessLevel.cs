namespace FitnessVibe.Domain.Enums
{
    /// <summary>
    /// Represents a user's fitness level for activity recommendations and progress tracking
    /// </summary>
    public enum FitnessLevel
    {
        /// <summary>
        /// Just starting their fitness journey
        /// </summary>
        Beginner = 1,

        /// <summary>
        /// Has some experience with fitness activities
        /// </summary>
        Intermediate = 2,

        /// <summary>
        /// Regular fitness enthusiast with good form and endurance
        /// </summary>
        Advanced = 3,

        /// <summary>
        /// Elite fitness level with significant experience
        /// </summary>
        Elite = 4
    }
}
