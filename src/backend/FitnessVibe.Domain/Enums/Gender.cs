namespace FitnessVibe.Domain.Enums
{
    /// <summary>
    /// Represents a user's gender identity
    /// </summary>
    public enum Gender
    {
        /// <summary>
        /// Prefer not to specify
        /// </summary>
        NotSpecified = 0,

        /// <summary>
        /// Male gender identity
        /// </summary>
        Male = 1,

        /// <summary>
        /// Female gender identity
        /// </summary>
        Female = 2,

        /// <summary>
        /// Non-binary gender identity
        /// </summary>
        NonBinary = 3,

        /// <summary>
        /// Other gender identity not listed
        /// </summary>
        Other = 4
    }
}
