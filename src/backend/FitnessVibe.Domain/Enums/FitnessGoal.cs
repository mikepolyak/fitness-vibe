namespace FitnessVibe.Domain.Enums
{
    /// <summary>
    /// Represents a user's primary fitness goal for personalized content and tracking
    /// </summary>
    public enum FitnessGoal
    {
        /// <summary>
        /// Focus on losing weight through a combination of exercise and nutrition
        /// </summary>
        WeightLoss = 1,

        /// <summary>
        /// Focus on building muscle mass and strength
        /// </summary>
        MuscleGain = 2,

        /// <summary>
        /// Focus on improving cardiovascular health and endurance
        /// </summary>
        CardioFitness = 3,

        /// <summary>
        /// Focus on improving flexibility and mobility
        /// </summary>
        Flexibility = 4,

        /// <summary>
        /// Focus on maintaining current fitness level and overall health
        /// </summary>
        Maintenance = 5,

        /// <summary>
        /// Focus on improving athletic performance for specific sports
        /// </summary>
        SportPerformance = 6,

        /// <summary>
        /// Focus on overall wellness and healthy lifestyle
        /// </summary>
        GeneralWellness = 7
    }
}
